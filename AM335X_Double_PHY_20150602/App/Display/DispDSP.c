/***********************************************************************************************
* Function	: DealWitchCheckCS
* Description	: CS校验
* Input		: 
* Output		: 
* Note(s)		: 
* Contributor	: 4/2/2014	宋超
***********************************************************************************************/
void DealWitchCheckCS(INT8U *RxBuff)
{
    switch(RxBuff[0])
    {
        case 0x01:
            PraperCheckCSFail();            
            break;
        case 0x02:
            PraperCheckCSStart();
            break;
        case 0x03:
            PraperCheckCSStage1Success();
            break;
        case 0x04:
            PraperCheckCSImageSuccess();
            break;
        default:
            PraperCheckCSFail();
            break;    
    }
}

