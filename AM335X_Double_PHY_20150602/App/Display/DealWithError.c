/***********************************************************************************************
* Function	: ErrorDisp
* Description	: 主菜单显示出错(实时跑钞)
* Input		: 
* Output	: 
* Note(s)	: 
* Contributor	: 17/06/2010	wangyao
***********************************************************************************************/	
void ErrorDisp(INT8U ErrorCode)
{
    switch(ErrorCode)
    {
        case 0x03:
            setcolor(0xF800,0xFFE0);
            fillw(CLEAR,0,180,480,270);
            printsstring32(0,231,"重张");
            break;
        case 0x07:
            setcolor(0xF800,0xFFE0);
            fillw(CLEAR,0,180,480,270);
            printsstring32(0,231,"连张");
            break;
        case 0x13:
            setcolor(0xF800,0xFFE0);
            fillw(CLEAR,0,180,480,270);
            printsstring32(0,231,"堵钞");
            break;
        case 0x14:
            setcolor(0xF800,0xFFE0);
            fillw(CLEAR,0,180,480,270);
            printsstring32(0,231,"停机保护");
            break;
        default:
            setcolor(0xF800,0xFFE0);
            fillw(CLEAR,0,180,480,270);
            printsstring32(0,231,"其它错误");
            break;
            
    }
}

